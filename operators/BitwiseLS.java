class BitwiseLS {
public static void main(String args[]) {
		int a = 9;
		System.out.println(a<<1);
		
		int b = 11;
		System.out.println(b<<1);
		
		int c = 21;
		System.out.println(c<<1);
		
		int p = 10;
		System.out.println(p<<2);
		
		int q = 13;
		System.out.println(q<<2);
		
		int r = 25;
		System.out.println(r<<3);
		
		int s = 12;
		System.out.println(s<<3);
	}
}