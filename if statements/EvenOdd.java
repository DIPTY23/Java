class EvenOdd 
{
	public static void main(String args[]) 
	{
		int n = 12;
		if(n%2 == 0) 
		{
			System.out.println(n + " is a Even no.");
		}
		else
		{
		System.out.println(n + " is an odd no.");
		}
		n = 17;
		if(n%2 == 0) 
		{
			System.out.println(n + " is a Even no.");
		}
		else
		{
		System.out.println(n + " is an odd no.");
		}
	}
}