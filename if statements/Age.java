class Age 
{
	public static void main(String args[]) 
	{
		int age = 7;
		if(age >= 18)
		{
			System.out.println(age + " is major");
		}
		else 
		{
			System.out.println(age + " is minor");
		}
		age = 25;
		if(age >= 18)
		{
			System.out.println(age + " is major");
		}
		else 
		{
			System.out.println(age + " is minor");
		}
	}
}